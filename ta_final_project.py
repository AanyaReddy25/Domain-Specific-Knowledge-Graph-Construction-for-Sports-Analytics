def token_to_entity_map(doc):
    """Return dict mapping token.i -> entity Span (or None)."""
    token_ent = {tok.i: None for tok in doc}
    for ent in doc.ents:
        for i in range(ent.start, ent.end):
            token_ent[i] = ent
    return token_ent


