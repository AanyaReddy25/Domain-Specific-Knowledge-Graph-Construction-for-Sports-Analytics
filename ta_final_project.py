# Building weighted NetworkX graph

G = nx.DiGraph()
edge_counter = Counter()
node_counter = Counter()

for s, rel, o in triples:
    edge_counter[(s, o, rel)] += 1
    node_counter[s] += 1
    node_counter[o] += 1

# Aggregate edges by (s,o) and collect multiple relation labels
for (s,o,rel), w in edge_counter.items():
    if not G.has_node(s): G.add_node(s)
    if not G.has_node(o): G.add_node(o)
    if G.has_edge(s,o):
        G[s][o]["weight"] += w
        G[s][o]["relations"].add(rel)
    else:
        G.add_edge(s,o, weight=w, relations=set([rel]))

print("Graph nodes:", G.number_of_nodes(), "edges:", G.number_of_edges())

# Optional: prune graph to reduce noise for plotting
# - keep only nodes with degree >= threshold or only top N edges by weight

# Keep only edges with weight >= 2 (tune this)
min_edge_weight = 2
H = nx.DiGraph()
for u,v,data in G.edges(data=True):
    if data.get("weight",1) >= min_edge_weight:
        H.add_node(u); H.add_node(v)
        H.add_edge(u,v, **data)

print("Pruned graph nodes:", H.number_of_nodes(), "edges:", H.number_of_edges())

# If pruning removes everything, fallback to original G
if H.number_of_nodes() == 0:
    H = G

